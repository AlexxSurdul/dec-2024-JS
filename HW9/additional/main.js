    const title = document.createElement('div');
    title.classList.add('course');
    title.innerText = course.title;

    const monthDuration = document.createElement('div');
    monthDuration.classList.add('monthDuration');
    monthDuration.innerText = course.monthDuration;

    const hourDuration = document.createElement('div');
    hourDuration.classList.add('hourDuration');
    hourDuration.innerText = course.hourDuration;

    const modules = document.createElement('ul');
    for (const module in course.modules) {
        let li = document.createElement('li');
        li.innerText = course.modules[module];
        modules.append(li);
    }

    const coursePage = document.createElement('div');
    coursePage.classList.add('coursePage');
    document.body.append(coursePage);
    coursePage.append(title, monthDuration, hourDuration, modules);

